       			<p>Memasuki musim penghujan, dinding rumah mesti menjadi perhatian. Pasalnya, guyuran air hujan yang terus menerus dapat berdampak buruk pada dinding rumah. Dinding akan menjadi lembab. Kelembaban tidak hanya terjadi pada dinding luar rumah yang langsung menerima guyuran air hujan, tetapi juga kerap merembes sampai ke dinding bagian dalam.</p>
						<p>Kelembaban yang terjadi pada dinding rumah tidak boleh diabaikan. Pasalnya, hal itu dapat memicu kerusakan-kerusakan lain yang lebih parah, misalnya lapisan cat menjadi mengapur, menggelembung atau mengelupas. Kerusakan tersebut terjadi karena tingginya kadar air di dalam dinding.</p>
						<p>Salah satu cara supaya dinding rumah tidak lembab selama musim penghujan, adalah dengan melapisi dinding dengan cat tembok tahan air. Namun sebelumnya, ada beberapa prosedur yang harus dilakukan sehingga hasil pengecatan bisa optimal.</p>
						<p>Pertama-tama, harus dipastikan bahwa kelembaban yang terjadi pada dinding tidak disebabkan oleh kebocoran. Jika ternyata kelembaban tersebut akibat kebocoran, maka kebocoran itulah yang wajib diselesaikan terlebih dahulu.</p>
						<p>Jika kondisi dinding belum lembab, maka pengecatan bisa langsung dilakukan. Sementara jika dinding sudah lembab, harus dipastikan terlebih dahulu bahwa cat lama yang masih menempel dalam kondisi baik dan tidak mudah mengelupas. Jika cat dinding ternyata sudah mengelupas, maka perlu dilakukan pengerokan hingga bersih sebelum memulai pengecatan.</p>
						<p>Untuk mendapatkan perlindungan optimal pada dinding rumah, disarankan supaya memilih produk cat dinding dengan kualitas prima, baik dalam hal ketahanan warna, maupun kerekatan lapisan. Terlebih, untuk dinding luar ruangan yang langsung terkena sinar matahari dan air hujan.</p>
						<p>Dalam hal ini, dua produk unggulan PT. Indaco Warna Dunia dapat menjadi pilihan yang tepat. Yang pertama adalah Belazo Climagard, cat eksterior yang tidak hanya memberikan perlindungan cuaca hingga tujuh tahun, tetapi juga memiliki ribuan pilhan warna menarik. Yang kedua adalah Top Seal, cat dengan lapisan elastis yang dapat menahan air hujan, sehingga tidak mudah meresap ke dalam lapisan tembok.</p>
