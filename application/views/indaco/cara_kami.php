       			<p><i>Nailhead Rusting</i> merupakan situasi di mana paku berkarat yang menancap ke dinding menimbulkan noda cokelat kemerahan di permukaan cat. Sebabnya, paku yang digunakan tidak dilapisi anti karat sehingga tidak tahan lama. Dalam mengatasi hal ini, Anda bisa mencegahnya dengan memilih paku yang sudah mengandung lapisan anti karat.</p>
						<p><strong>Bagaimana memperbaiki dinding yang sudah kena <i>nailhead rusting</i>?</strong></p>
						<p>Memperbaiki dinding dengan <i>nailhead rusting</i> memang tidak mudah, karena tidak cukup dengan langsung melapisi ulang dengan cat baru. Berikut beberapa hal yang bisa Anda terapkan:</p>
						<ol>
							<li>Ampelas bagian dinding yang kena karat atau <i>nailhead rusting</i>. Ini adalah satu cara untuk membersihkan bekas karat hingga tidak bersisa.</li>
							<li>Melapisi dengan primer anti karat. Dalam hal ini Anda bisa gunakan <strong>Envi Anti Corrosive Primer</strong>. Lalu cat ulang dengan <strong>Envi Wall Putty</strong>.</li>
							<li>Lalu Anda bisa memaku ulang dengan paku baru. Tentunya yang mengandung lapisan anti karat, atau berbahan galvanis.</li>
